window.addEventListener("hashchange", function () {
  let container = document.querySelector(".container");
  let personalPage = document.querySelector(".box");
  if (window.location.hash.includes("bioPage")) {
    container.classList.add("hidden");
    personalPage.classList.remove("hidden");
    //vercnel hash id , programmers[id]
    let hash = window.location.hash.split("=");
    let id = hash[1]; // popokhel
    console.log ("id = ", id);
    addUserData(programmers[id - 1]);
  } else {
    container.classList.remove("hidden");
    personalPage.classList.add("hidden");
  }
});
function addUserData(programmer) {
  console.log("personalData===", programmer);
  document.querySelector(".cvName").innerHTML = programmer.firstName + " " + programmer.lastName;
  document.querySelector(".email").innerHTML = programmer.email;
  document.querySelector(".education").innerHTML = programmer.education === "undefined" ? "notProvited": programmer.education ;
  document.querySelector(".workExperience").innerHTML = programmer.workExperience === "undefined" ? "notProvited": programmer.workExperience;
  document.querySelector(".phone").innerHTML = programmer.phone;
  document.querySelector(".image img").src = programmer.gender === "Male" ? "./img/boy.png" : "./img/girl.png";
  document.querySelector("#trainings").innerHTML = programmer.trainings;
}
